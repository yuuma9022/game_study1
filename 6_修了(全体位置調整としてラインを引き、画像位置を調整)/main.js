function DrawMain(){
    const g = gScreen.getContext("2d");

    for(let y = 0 ;y < 30; y++){
        for(let x = 0 ;x < 30; x++){
            let px = gPlayerX + x;
            let py = gPlayerY + y;
            //x軸の値をx * TILESIZE - TILESIZE / 2にすることで、キャラ、キャンバスのずれを１つ分修正
            DrawTile( g, x * TILESIZE - TILESIZE / 2, y * TILESIZE, gMap[ py * MAP_EIDTH + px ]);
        }
    }

    //縦横ラインの色を赤色に設定
    g.fillStyle = "#ff0000";
    //横線をペイント(x始点,y始点,x終点,y終点)
    g.fillRect(0, HEIGHT / 2 - 1, WIDTH, 2);
    //縦線をペイント(x始点,y始点,x終点,y終点)
    g.fillRect(WIDTH / 2 - 1, 0, 2, HEIGHT);

    //キャラクター画像の切り取りと貼り付け(キャラ画像,xの切取始点,yの切取始点,xの切取大きさ,yの切取大きさ,xの貼付始点,yの貼付始点,xの貼付大きさ,yの貼付大きさ)
    // WIDTH / 2 - CHRWIDTH / 2, HEIGHT / 2 - CHRHEIGHT + TILESIZE / 2（縦はキャラ画像が奇数設定であるため、ずれないよう調整）　で背景画像の中央位置を指定
    g.drawImage( gImgPlayer, CHRWIDTH, 0, CHRWIDTH, CHRHEIGHT,
                 WIDTH / 2 - CHRWIDTH / 2, HEIGHT / 2 - CHRHEIGHT + TILESIZE / 2, CHRWIDTH, CHRHEIGHT);
}



function DrawTile( g, x, y, idx){
    const ix = ( idx % TILECOLUMN ) * TILESIZE;
    const iy = Math.floor( idx / TILECOLUMN ) * TILESIZE;
    g.drawImage( gImgMap, ix, iy, TILESIZE, TILESIZE, x, y, TILESIZE, TILESIZE);
} 


function LoadImage(){
    gImgMap = new Image();
    gImgMap.src = gFileMap;
    gImgPlayer = new Image();
    gImgPlayer.src = gFiePlayer;
}


function WmPaint(){
    DrawMain();

    const ca = document.getElementById("main");
    const g  = ca.getContext("2d");

    g.drawImage(gScreen, 0, 0, gScreen.width, gScreen.height, 0, 0, gWidth, gHeight);
}


function WmSize(){
    const ca = document.getElementById("main");
    ca.width = window.innerWidth;
    ca.height = window.innerHeight;
    const g  = ca.getContext("2d");

    g.imageSmoothingEnabled = g.msImageSmoothingEnabled = SMOOTH;

    gWidth = ca.width;
    gHeight = ca.height;

    if(gWidth / WIDTH < gHeight / HEIGHT){
        gHeight = gWidth * WIDTH / HEIGHT;
    }else{
        gWidth = gHeight * WIDTH / HEIGHT;
    }
}



function WmTimer(){
    gFrame++;
    WmPaint();
}


window.onkeydown = function(ev){
    let c = ev.keyCode;
    if(c == 37) gPlayerX--;
    if(c == 38) gPlayerY--;
    if(c == 39) gPlayerX++;
    if(c == 40) gPlayerY++;
}


